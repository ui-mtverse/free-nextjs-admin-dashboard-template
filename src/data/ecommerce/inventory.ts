export const inventorySummary = {
  total: inventoryItems.reduce((s, i) => s + i.onHand, 0),
  inStock: inventoryItems.filter((i) => i.status === "In stock").length,
  lowStock: inventoryItems.filter((i) => i.status === "Low stock").length,
  outOfStock: inventoryItems.filter((i) => i.status === "Out of stock").length,
  backordered: inventoryItems.filter((i) => i.status === "Backordered").length,
  inventoryValue: inventoryItems.reduce((s, i) => s + i.onHand * i.cost, 0),
  retailValue: inventoryItems.reduce((s, i) => s + i.onHand * i.retail, 0),
};
