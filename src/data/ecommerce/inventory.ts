const img = (seed: string, w = 80, h = 80) =>
  `https://picsum.photos/seed/${encodeURIComponent(seed)}/${w}/${h}`;

const movement = (base: number): { date: string; in: number; out: number }[] => {
  const days = ["Mon", "Tue", "Wed", "Thu", "Fri", "Sat", "Sun"];
  return days.map((d, i) => ({
    date: d,
    in: i % 3 === 0 ? Math.round(base * 0.2) : 0,
    out: Math.round(base * (0.05 + (i % 4) * 0.02)),
  }));
};

